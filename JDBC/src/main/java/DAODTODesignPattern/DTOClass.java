package DAODTODesignPattern;

public class DTOClass
{
    private int guitarStudId;
    private String guitarStudName;
    private String guitarClassDiv;
    private double guitarClassFees;

    public int getGuitarStudId() {
        return guitarStudId;
    }

    public void setGuitarStudId(int guitarStudId) {
        this.guitarStudId = guitarStudId;
    }

    public String getGuitarStudName() {
        return guitarStudName;
    }

    public void setGuitarStudName(String guitarStudName) {
        this.guitarStudName = guitarStudName;
    }

    public String getGuitarClassDiv() {
        return guitarClassDiv;
    }

    public void setGuitarClassDiv(String guitarClassDiv) {
        this.guitarClassDiv = guitarClassDiv;
    }

    public double getGuitarClassFees() {
        return guitarClassFees;
    }

    public void setGuitarClassFees(double guitarClassFees) {
        this.guitarClassFees = guitarClassFees;
    }
}
