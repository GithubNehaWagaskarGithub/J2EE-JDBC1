package DAODTODesignPattern1;

public class DTOClass {
    private int gId;
    private String gName;
    private String gDiv;
    private double gFees;

    public int getgId() {
        return gId;
    }

    public void setgId(int gId) {
        this.gId = gId;
    }

    public String getgName() {
        return gName;
    }

    public void setgName(String gName) {
        this.gName = gName;
    }

    public String getgDiv() {
        return gDiv;
    }

    public void setgDiv(String gDiv) {
        this.gDiv = gDiv;
    }

    public double getgFees() {
        return gFees;
    }

    public void setgFees(double gFees) {
        this.gFees = gFees;
    }
}
